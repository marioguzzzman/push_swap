#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	if (lst == NULL)
		return (NULL);
	while (lst->next != NULL)
		lst = lst->next;
	return (lst);
}
/*
DESCRIPTION
Returns the last node of the list.

PARAMETERS
lst: The beginning of the list.

RETURN VALUES
Last node of the list

NOTES:
*/
