#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	write(fd, s, ft_strlen(s));
	write(fd, "\n", 1);
}

/*
DESCRIPTION
outputs a string to a specified file deescriptor folllowed by a new line

RETURN VALUES
None
*/
